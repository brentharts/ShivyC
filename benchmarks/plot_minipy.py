#!/usr/bin/env python3
"""Render the minipy benchmark results to PDF figures and a LaTeX section.

Reads benchmarks/results/minipy_results.json (written by
run_minipy_benchmarks.py) and writes, into the output directory (default
/tmp/shivyc_benchmarks, override with $BENCH_PLOT_DIR or argv[1]):

  * minipy_summary.png / .pdf -- two panels: runtime normalised to CPython and
                                 peak RSS (MB), CPython vs minipy-native.
  * minipy_body.tex           -- a self-contained intro section ("What is
                                 minipy?") plus a results table; tools/
                                 benchmarks.tex \\input's it as the first section.
"""
import json
import os
import sys

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
RESULTS = os.path.join(HERE, "results", "minipy_results.json")

CPY = "#4c72b0"
MINI = "#dd8452"


def _fmt(v, nd=3):
    if v is None:
        return "--"
    if v >= 100:
        return "%.0f" % v
    return ("%." + str(nd) + "g") % v


def summary_figure(benches, out_dir):
    names = [b["benchmark"] for b in benches]
    y = np.arange(len(names))
    # runtime normalised to CPython (=1)
    ratio = []
    for b in benches:
        c = b["backends"]["cpython"]["runtime_s"]
        m = b["backends"]["minipy"]["runtime_s"]
        ratio.append(m / c if c else 0.0)
    cpy_mb = [b["backends"]["cpython"]["rss_kb"] / 1024.0 for b in benches]
    mini_mb = [b["backends"]["minipy"]["rss_kb"] / 1024.0 for b in benches]

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(10, 4.2))

    ax1.barh(y, ratio, color=MINI, height=0.6)
    ax1.axvline(1.0, ls="--", color="gray", lw=1)
    ax1.set_yticks(y)
    ax1.set_yticklabels(names, fontsize=8)
    ax1.invert_yaxis()
    ax1.set_xlabel("minipy runtime / CPython runtime (lower is better)")
    ax1.set_title("Runtime relative to CPython")
    for yi, v in zip(y, ratio):
        ax1.text(v, yi, "  %.2gx" % v, va="center", fontsize=7)

    h = 0.38
    ax2.barh(y - h / 2, cpy_mb, height=h, color=CPY, label="CPython")
    ax2.barh(y + h / 2, mini_mb, height=h, color=MINI, label="minipy")
    ax2.set_yticks(y)
    ax2.set_yticklabels(names, fontsize=8)
    ax2.invert_yaxis()
    ax2.set_xscale("log")
    ax2.set_xlabel("peak RSS (MB, log scale)")
    ax2.set_title("Peak resident memory")
    ax2.legend(fontsize=8, loc="lower right")

    fig.tight_layout()
    for ext in ("png", "pdf"):
        fig.savefig(os.path.join(out_dir, "minipy_summary." + ext),
                    bbox_inches="tight")
    plt.close(fig)


INTRO = r"""% Auto-generated by plot_minipy.py -- do not edit by hand.
\section{minipy: a tiny Python interpreter built with the toolchain}

\textbf{minipy} is a small Python interpreter that is itself written in the
\texttt{py2c} Python subset (``ShivyCX''/RPython) and compiled to C by
\texttt{tools/py2c.py}. It closes the loop of the whole toolchain: the same
\texttt{.py}$\rightarrow$C transpiler that the rest of this report benchmarks is
used to turn an interpreter \emph{source} into a native ELF, which then runs
Python programs of its own.

The pipeline has three stages that must agree byte-for-byte on every program:

\begin{enumerate}
  \item a CPython-side \textbf{compiler} (\texttt{tools/minipy/compiler.py})
        lowers Python source through the AST into a flat register bytecode;
  \item a pure-Python \textbf{reference VM} executes that bytecode (the
        correctness oracle); and
  \item the \textbf{native interpreter} (\texttt{tools/minipy/interp.py}),
        \texttt{py2c}-compiled to C, runs the same bytecode at speed.
\end{enumerate}

Values are a 16-byte tagged union (\texttt{tag} + an overlapping
\texttt{int}/\texttt{double}/\texttt{char*} slot, a py2c anonymous-union POD);
small integers, \texttt{None}, the booleans, and program constants are interned;
register frames, call-argument lists, and the exception block-stack are pooled or
shared so that hot recursion allocates almost nothing. The benchmarks below run
the same eight programs under CPython and under the \texttt{py2c}-compiled
interpreter, checking that their output is identical and recording best-of-$N$
runtime and exact peak resident memory.

"""


def write_body(benches, out_dir):
    L = [INTRO]
    a = L.append
    a(r"\begin{table}[h]\centering\small")
    a(r"\caption{minipy native interpreter vs.\ CPython: best-of-$N$ runtime, "
      r"peak RSS, and one-time bytecode compile time. Every row's output is "
      r"byte-identical between the two.}")
    a(r"\begin{tabular}{lrrr@{\hskip 2em}rr@{\hskip 2em}r}\toprule")
    a(r"& \multicolumn{3}{c}{runtime (s)} & \multicolumn{2}{c}{peak RSS (MB)} "
      r"& \\")
    a(r"\cmidrule(lr){2-4}\cmidrule(lr){5-6}")
    a(r"benchmark & CPython & minipy & ratio & CPython & minipy "
      r"& compile (s)\\\midrule")
    for b in benches:
        be = b["backends"]
        c = be["cpython"]
        m = be["minipy"]
        ratio = (m["runtime_s"] / c["runtime_s"]) if c["runtime_s"] else None
        a("%s & %s & %s & %s & %s & %s & %s\\\\" % (
            b["benchmark"].replace("_", r"\_"),
            _fmt(c["runtime_s"]), _fmt(m["runtime_s"]),
            (_fmt(ratio, 2) + r"$\times$") if ratio else "--",
            _fmt(c["rss_kb"] / 1024.0), _fmt(m["rss_kb"] / 1024.0),
            _fmt(m.get("compile_s"))))
    a(r"\bottomrule\end{tabular}\end{table}")
    a(r"\begin{figure}[h]\centering")
    a(r"\includegraphics[width=\linewidth]{minipy_summary.pdf}")
    a(r"\caption{minipy-native vs.\ CPython across the eight programs: runtime "
      r"relative to CPython (left; below $1\times$ is faster than CPython) and "
      r"peak resident memory (right, log scale).}")
    a(r"\end{figure}")
    a(r"\clearpage")
    path = os.path.join(out_dir, "minipy_body.tex")
    with open(path, "w") as f:
        f.write("\n".join(L) + "\n")
    return path


def main():
    out_dir = (sys.argv[1] if len(sys.argv) > 1
               else os.environ.get("BENCH_PLOT_DIR", "/tmp/shivyc_benchmarks"))
    os.makedirs(out_dir, exist_ok=True)
    with open(RESULTS) as f:
        benches = json.load(f)
    summary_figure(benches, out_dir)
    body = write_body(benches, out_dir)
    print("Wrote", body, "and minipy_summary.{png,pdf} to", out_dir)


if __name__ == "__main__":
    main()
