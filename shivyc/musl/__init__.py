"""Packaged musl, materialized on demand.

The musl fork's headers and C sources are stored as Python string modules in
this package (generated by ``tools/pack_musl.py``). At build time we write the
files we need to a directory under ``/tmp`` and hand back the include-path flags
so a program can be compiled against musl, bypassing glibc, without keeping a
copy of the musl tree in the ShivyC source checkout.

Typical use::

    from shivyc import musl
    tree = musl.materialize()                 # writes headers to /tmp/...
    cflags = tree.public_cflags()             # -D... -I... for user code
    src = tree.write_source("string", "strlen.c")   # extract one .c
    # ...compile `src` with tree.internal_cflags(), link, etc.

Only the headers are written eagerly (the whole include tree must be present
for ``#include`` resolution); C sources are written individually as requested,
which is what lets us compile "only the parts the application needs."
"""
from __future__ import annotations

import importlib
import os
import tempfile

from shivyc.musl import _manifest

# Map category name -> module name (e.g. "string" -> "string"), and remember
# the per-category source list lazily.
_CAT_TO_MOD = {cat: mod for cat, mod, _n in _manifest.CATEGORIES}


def categories() -> list:
    """Return the list of packaged musl source categories (e.g. 'string')."""
    return [cat for cat, _mod, _n in _manifest.CATEGORIES]


def _category_module(category: str):
    mod = _CAT_TO_MOD.get(category)
    if mod is None:
        raise KeyError("no packaged musl category %r" % category)
    # Fully-qualified import avoids colliding with stdlib names like errno,
    # time, signal (which would shadow a bare `import errno`).
    return importlib.import_module("shivyc.musl." + mod)


def sources(category: str) -> list:
    """Return the .c file names packaged for a category."""
    return sorted(_category_module(category).FILES.keys())


def _write_file(path: str, text: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    # Only write if absent or changed, so repeated builds reuse the tree.
    if os.path.exists(path):
        with open(path, "r", encoding="utf-8", errors="surrogateescape") as f:
            if f.read() == text:
                return
    with open(path, "w", encoding="utf-8", errors="surrogateescape") as f:
        f.write(text)


class MuslTree:
    """A materialized musl directory on disk plus its include-path flags."""

    def __init__(self, root: str) -> None:
        self.root = root

    # -- include flags -------------------------------------------------
    def _dirs(self, rels) -> list:
        return [os.path.join(self.root, d) for d in rels]

    def public_include_dirs(self) -> list:
        """Header dirs for compiling USER code against musl (public only)."""
        return self._dirs(_manifest.PUBLIC_HEADER_DIRS)

    def internal_include_dirs(self) -> list:
        """Header dirs for compiling MUSL'S OWN sources (adds internal)."""
        return self._dirs(_manifest.INTERNAL_HEADER_DIRS
                          + _manifest.PUBLIC_HEADER_DIRS)

    def defines(self) -> list:
        return list(_manifest.DEFAULT_DEFINES)

    def public_cflags(self) -> list:
        out = ["-D" + d for d in self.defines()]
        for d in self.public_include_dirs():
            out += ["-I", d]
        return out

    def internal_cflags(self) -> list:
        out = ["-D" + d for d in self.defines()]
        for d in self.internal_include_dirs():
            out += ["-I", d]
        return out

    # -- source extraction ---------------------------------------------
    def write_source(self, category: str, name: str) -> str:
        """Write one packaged .c into the tree and return its path."""
        files = _category_module(category).FILES
        if name not in files:
            raise KeyError("no packaged musl source src/%s/%s"
                           % (category, name))
        path = os.path.join(self.root, "src", category, name)
        _write_file(path, files[name])
        return path

    def write_category(self, category: str) -> list:
        """Write every packaged .c for a category; return their paths."""
        return [self.write_source(category, n) for n in sources(category)]


_HEADERS_WRITTEN: dict = {}


def materialize(dest: str = None) -> MuslTree:
    """Write the full musl header tree to `dest` (a temp dir by default).

    Returns a MuslTree. Headers are written once per destination and reused on
    subsequent calls, so this is cheap to call repeatedly in a build.
    """
    if dest is None:
        dest = os.path.join(tempfile.gettempdir(), "shivyc-musl-tree")
    dest = os.path.abspath(dest)

    if not _HEADERS_WRITTEN.get(dest):
        headers = importlib.import_module("shivyc.musl._headers").HEADERS
        for rel, text in headers.items():
            _write_file(os.path.join(dest, rel), text)
        _HEADERS_WRITTEN[dest] = True
    return MuslTree(dest)
